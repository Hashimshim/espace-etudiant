    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //validate data
        $request->validate([
            'nom' => 'required',
            'prenom' => 'required',
            'CIN' => 'required',
            'CNE' => 'required',
            'apogee' => 'required',
            'email' => 'required',
            'niveau' => 'required',
            'filiere' => 'required',
            'type' => 'required'
            //'details' => 'required'
        ]);
        //create
        Document::create($request->all());

        //redirect
        return redirect('document')->with('success','Votre demande a été effectué.');
    }
    
    



